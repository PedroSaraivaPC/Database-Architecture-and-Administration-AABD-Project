        RAISE PRODUTO_NAO_EXISTE;
    END IF;
    
    SELECT LATITUDE, LONGITUDE INTO LAT1, LON1 FROM MAQUINA WHERE ID_MAQUINA = IDMAQUINA;
    FOR R IN C1 LOOP
